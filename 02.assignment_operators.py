"""
assignment operators

=   Assignment
+=  addition
-=  subtraction
*=  multiplication
/=  division
%=  modulus
**= exponentiation
//= floor division

"""
a=125
print(a)
a+=5
print(a)
a-=5
print(a)
a*=5
print(a)
a/=5
print(a)

b=134
b%=5
print(b)

c=2
c**=5
print(c)
d=158
d//=5
